    const toggleImage1 = document.getElementById('infoToggle1');
   const toggleImage2 = document.getElementById('infoToggle2');
   const toggleImage3 = document.getElementById('infoToggle3');
   const toggleImage4 = document.getElementById('infoToggle4');
   const toggleImage5 = document.getElementById('infoToggle5');
   // Get the info box
   const infoBox = document.getElementById('infoBox');

   function toggleSection(imgEl, boxEl) {
      if (!imgEl || !boxEl) return;
      boxEl.classList.toggle('hidden');
      imgEl.classList.toggle('rotated');
   }

   function attachToggle(imgEl, boxEl) {
      if (!imgEl || !boxEl) return;
      imgEl.addEventListener('click', function (event) {
         event.stopPropagation();
         toggleSection(imgEl, boxEl);
      });
   }

   attachToggle(toggleImage1, infoBox1);
   attachToggle(toggleImage2, infoBox2);
   attachToggle(toggleImage3, infoBox3);
   attachToggle(toggleImage4, infoBox4);
   attachToggle(toggleImage5, infoBox5);

